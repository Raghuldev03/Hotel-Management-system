    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String FirstName=jTextField2.getText();
        String LastName=jTextField3.getText();
        String Phone_no=jTextField4.getText();
        String Email=jTextField5.getText();
        
        if(FirstName.trim().equals("") || LastName.trim().equals("") || Phone_no.trim().equals("") ||Email.trim().equals("") )
        {
            JOptionPane.showMessageDialog(rootPane,"Required Fields : FirstName // Last Name // Phone number // Email","Empty Fields",JOptionPane.WARNING_MESSAGE);
        }
        else{
            if(clients.addClients(FirstName, LastName, Phone_no, Email))
            {
            JOptionPane.showMessageDialog(rootPane,"New Client Added Successfully","Add Client",JOptionPane.INFORMATION_MESSAGE);
            }
            else{
                JOptionPane.showMessageDialog(rootPane,"Client  Not Added ","Add  Client Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

