    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int ID=0;
        String FirstName=jTextField2.getText();
        String LastName=jTextField3.getText();
        String Phone_no=jTextField4.getText();
        String Email=jTextField5.getText();
        if(FirstName.trim().equals("") || LastName.trim().equals("") || Phone_no.trim().equals("") ||Email.trim().equals("") )
        {
            JOptionPane.showMessageDialog(rootPane,"Required Fields : FirstName // Last Name // Phone number // Email","Empty Fields",JOptionPane.WARNING_MESSAGE);
        }
        else{
            try{
                ID =Integer.valueOf(jTextField1.getText());
                if(clients.editClients(ID,FirstName, LastName, Phone_no, Email))
                {
                    JOptionPane.showMessageDialog(rootPane,"Client details updated Successfully","Edit Client",JOptionPane.INFORMATION_MESSAGE);
                }
                else{
                JOptionPane.showMessageDialog(rootPane,"Client details Not updated ","Edit Client Error",JOptionPane.ERROR_MESSAGE);
                }
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(rootPane,ex.getMessage()+"- Enter client ID (numbers) "," Client ID Error",JOptionPane.ERROR_MESSAGE);
            } 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

